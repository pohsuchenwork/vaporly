// Vaporly appearance, one shared helper used by both webview windows (main and
// overlay). v2 ships SIX curated accent presets, each a pair of verified
// light/dark stop sets derived from the sakura anatomy at matched OKLCH
// lightness/chroma with the deep stops darkened per hue until the contrast
// gates pass (white-on-solid >= 4.5, accent ink >= 4.5 on the light surfaces,
// focus ring >= 3). Painting the stops inline on :root guarantees a flash-free
// first paint; the compat aliases in theme.css carry --color-accent to the
// legacy --color-logo-primary / --color-background-ui names the overlay still
// reads. The theme MODE (system/light/dark) rides the same helper: it stamps
// data-theme on the root element, which the theme.css selectors key off.

