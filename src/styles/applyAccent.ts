/** The default (light sakura) accent, kept as a stable export. */
export const DEFAULT_ACCENT = PRESETS.sakura.light.accent;

// The applied appearance. Defaults match a fresh install (system + sakura) so
// the pre-settings first paint is correct without a store read.
let currentPreset: AccentPreset = "sakura";

/**
 * True when the effective theme is dark: OS dark unless data-theme forces light,
 * or data-theme explicitly forces dark. Mirrors the CSS selector logic in
 * theme.css so JS and CSS never disagree.
 */
function isDark(): boolean {
  const forced = document.documentElement.dataset.theme;
  const osDark = window.matchMedia("(prefers-color-scheme: dark)").matches;
  return (osDark && forced !== "light") || forced === "dark";
}

/** Paint the current preset's stops for the current theme onto :root. */
function paint(): void {
  const pair = PRESETS[currentPreset] ?? PRESETS.sakura;
  const s = isDark() ? pair.dark : pair.light;
  const root = document.documentElement.style;
  root.setProperty("--color-accent", s.accent);
  root.setProperty("--color-accent-ink", s.accentInk);
  root.setProperty("--color-accent-tint", s.tint);
  root.setProperty("--color-accent-solid", s.solid);
  root.setProperty("--color-accent-solid-hover", s.solidHover);
  root.setProperty("--color-accent-solid-active", s.solidActive);
  root.setProperty("--color-logo-stroke", s.stroke);
  root.setProperty("--color-focus-ring", s.focusRing);
  root.setProperty("--color-surface-selected", s.surfaceSelected);
}

/**
 * Apply a stored appearance: stamp (or clear) data-theme for the mode, adopt
 * the accent preset, and repaint. Call whenever the settings load or change.
 */
export function applyAppearance(mode: ThemeMode, preset: AccentPreset): void {
  const root = document.documentElement;
  if (mode === "system") {
    delete root.dataset.theme;
  } else {
    root.dataset.theme = mode;
  }
  currentPreset = PRESETS[preset] ? preset : "sakura";
  paint();
}

/** Re-apply the accent for the current theme. Call after flipping data-theme. */
export function repaintAccent(): void {
  paint();
}

/**
 * Bootstrap for a window entry: paint the default appearance synchronously
 * (no flash) and re-paint when the OS light/dark mode flips. Safe to call in
 * any window; the stored appearance is applied on top once settings load.
 */
export function bootstrapAccent(): void {
  paint();
  window
    .matchMedia("(prefers-color-scheme: dark)")
    .addEventListener("change", () => paint());
}
